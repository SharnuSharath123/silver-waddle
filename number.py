# NumPy
# NumPy is used for numerical computations and working with arrays.

# Example: Basic Operations with NumPy

import numpy as np

# Create a NumPy array
arr = np.array([1, 2, 3, 4, 5])

# Perform operations
mean = np.mean(arr)
std_dev = np.std(arr)

print(f"Array: {arr}")
print(f"Mean: {mean}")
print(f"Standard Deviation: {std_dev}")
